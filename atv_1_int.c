/*
* Faça um programa que leia um número inteiro e o imprima. 
*/
#include <stdio.h>

int main() {
	//definindo variáveis
	int n1;
	
	printf("digite um numero inteiro: \n");
	
	//recebendo valor
	scanf("%d",&n1);
	
	//impriminto valor recebido
	printf("seu numero: %d",n1);
	
	return 0;
}
